
public class SwitchStatement {
public static void main(String args[]) {
	
	String name = "Mahesh";
	switch(name){
	case "Ram":
		System.out.println("My name is Ram.");
		break;
	case "Ravi":
		System.out.println("My name is Ravi.");
		break;
	case "Mahesh":
		System.out.println("My name is Mahesh.");
		
		break;
default:
		System.out.println("MY name is Hari.");
		
		
		
		
		
	}
	
	
	
	
	
	
	
	
	
}
}
